package example;

public class NumberOperationSample2 {
    public static void main(String[] args) {
        int num1= 4;
        int num2 =2;
        System.out.println(num1 + num2);
        System.out.println(num1 - num2);
        System.out.println(num1 * num2);
        System.out.println(num1 / num2);

        double num3 = 4.0;
        double num4 = 0.5;
        System.out.println(num3 + num4);
        System.out.println(num3 - num4);
    }
}
